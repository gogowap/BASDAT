-- Durasi Terpendek & Umur Termuda
SELECT 
    f.JUDUL || ' adalah film dengan durasi terpendek yaitu ' || 
    f.DURASI || ' Menit, yang ditonton dengan umur termuda, yaitu ' || 
    MIN(EXTRACT(YEAR FROM TO_DATE('2020-01-01', 'YYYY-MM-DD')) - EXTRACT(YEAR FROM m.TGL_LAHIR)) || ' Tahun' 
    AS KETERANGAN
FROM FILM_103122430010 f
JOIN JADWAL_103122430010 j ON f.ID_FILM = j.ID_FILM
JOIN TRANSAKSI_103122430010 t ON j.ID_JADWAL = t.ID_JADWAL
JOIN MEMBER_103122430010 m ON t.ID_MEMBER = m.ID_MEMBER
WHERE f.DURASI = (SELECT MIN(DURASI) FROM FILM_103122430010)
GROUP BY f.JUDUL, f.DURASI;

-- Teater dengan Pemakaian Terbanyak
SELECT
    th.ID_THEATER, 
    th.KELAS, 
    count(t.KODE_PEMESANAN) AS TOTAL_PEMAKAIAN
FROM THEATER_103122430010 th
JOIN JADWAL_103122430010 j ON th.ID_THEATER = j.ID_THEATER
JOIN TRANSAKSI_103122430010 t ON j.ID_JADWAL = t.ID_JADWAL
GROUP BY th.ID_THEATER, th.KELAS
HAVING count(t.KODE_PEMESANAN) = (
    SELECT MAX(COUNT(t2.KODE_PEMESANAN))
    FROM TRANSAKSI_103122430010 t2
    JOIN JADWAL_103122430010 j2 ON t2.ID_JADWAL = j2.ID_JADWAL
    GROUP BY j2.ID_THEATER
);